                            <div class="text">
                                I give praise my heavenly father because He is worthy to be praised. I have never thought of working in expanding God’s kingdom. From my early childhood I have tremendous interest about stage drama. In my childhood I had a dream of becoming a drama artist. I never thought that we can give glory to God through drama. In 2009, 1st July I joined Sacrifice Ministry as a full time minister. However, I never thought that God will use my childhood dream for expanding His kingdom! 
                            </div>
                            <div class="text">
                                In November 2009, we went to Kustia for a drama performance. We were 13 people in the team. I had a lot of chance of doing stage drama in my life but I will never be able to forget that day’s memory. The drama was staged in a school’s playground. Thousands of people had watched the drama. The theme of that drama was liberation was but God’s word was there as well. I got the evidence of the strength of God’s word.
                            </div>
                            <div class="text">
                               Later I got to know that after watching that drama a lot of people came to the local church (through which we went there) and discussed details about the subject of the drama with the Pastor. Eventually, new believers are formed there. Thus we show our drama in different parts of the country. 
                            </div>
                            <div class="text">
                                We generally prepare our drama regarding social inconsistency. But inside that we have biblical values and word of God. Drama can also be a part of spreading gospel. It is easy to enter into the very difficult areas with drama. Many truths can be said easily through drama.   
                            </div>
                            <div class="text">
                                Till now we have done 3 dramas in around 12 districts of Bangladesh. Along with that we have a training program for drama workers. We call this training “Theater for Transforming Community”. It’s a basic course for 5 days. In first 4 days we give a basic idea about drama and dramatic theory and in the last day we give training about incorporating biblical values in drama. I give glory to God that he used a common person like me for changing the society.
